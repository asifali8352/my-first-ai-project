# Functions are like recipes - they do something when you call them
def say_hello_to(name):
    print("Hello", name + "! Nice to meet you!")
    print("Welcome to my AI learning journey!")

def calculate_dog_age(human_age):
    dog_age = human_age * 7
    print("If you were a dog, you'd be", dog_age, "years old!")

# Let's use our functions!
say_hello_to("Mom")
say_hello_to("Friend")
calculate_dog_age(35)  # Replace 10 with your age
